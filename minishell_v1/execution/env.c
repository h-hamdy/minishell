#include "../minishell.h"

void	ft_env()
{
	int i;

	i = 0;
	while (g_v.envp[i])
	{
		if (g_v.envp[i][0])
		{
			ft_putstr_fd(g_v.envp[i], 1);
			ft_putstr_fd("\n", 1);
		}
		i++;
	}
}
